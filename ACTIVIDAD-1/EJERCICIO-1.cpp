#include <iostream>
using namespace std;

int main() {
    float num1, num2, suma;

    cout << "Ingrese el primer número: ";
    cin >> num1;

    cout << "Ingrese el segundo número: ";
    cin >> num2;

    suma = num1 + num2;

    cout << "La suma de " << num1 << " y " << num2 << " es: " << suma << endl;

    return 0;
}
